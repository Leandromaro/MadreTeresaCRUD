    private String calculaCuil(String c) {
        String cuil = null;
        if (!c.isEmpty()) {
            String serie = "5432765432";
            int acum = 0;
            String cuilConcat = "";
            int mod;
            String s;
            String c1;
            for (int i = 0; i < serie.length(); i++) {
                s = "" + serie.charAt(i);
                c1 = "" + c.charAt(i);
                acum = acum + (Integer.valueOf(s) * Integer.valueOf(c1));
            }
            mod = acum % 11;
            if (mod == 0) {
                cuil = c + "0";
            } else if (mod == 1) {
                cuil = c + "9";
            } else {
                int r = 11 - mod;
                cuil = c + r;
            }
            for (int i = 0; i < cuil.length(); i++) {

                if (i == 2 || i == 10) {
                    cuilConcat += "-" + cuil.charAt(i);
                } else {
                    cuilConcat += cuil.charAt(i);
                }

            }
            cuil = cuilConcat;
        }
        return cuil;
    }

