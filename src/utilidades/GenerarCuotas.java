    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        parent.setEntity();
        boolean band = parent.generarCuotas(jMonthChooser1.getMonth(),jYearChooser1.getYear());
        int mes =jMonthChooser1.getMonth()+1;
        if(band==true)
          JOptionPane.showMessageDialog(null,"Se generaron correctamente las cuotas correspondiente al "+mes+"/"+jYearChooser1.getYear()+".");
        else{
              JOptionPane.showMessageDialog(null,"Las cuotas no fueron generadas porque ya existen!");
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

