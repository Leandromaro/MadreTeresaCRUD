    private void jBGenReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGenReportActionPerformed
          
        String [] fd = jTFDesde.getText().split("/");
        String [] fh = jTFHasta.getText().split("/");
        Double totCuota =0.0;
        Double totDonac =0.0;
        DecimalFormat df = new DecimalFormat("0.00##");        
        String sql = "SELECT MONTH(fechaPago) mes,SUM(monto) monto FROM cuotaSocial WHERE fechaPago BETWEEN '"+fd[2]+"-"+fd[1]+"-"+fd[0]+"' AND '"+
                fh[2]+"-"+fh[1]+"-"+fh[0]+"' AND fechaPago IS NOT NULL GROUP BY MONTH(fechaPago)";
         List lista = new ArrayList();
         //Cuotas Sociales
        try {
             Statement st = cn.createStatement();
             ResultSet rs = st.executeQuery(sql);             
             while(rs.next()){
                 totCuota=totCuota+rs.getDouble("monto");
                 ListaIngresos ingresos= new ListaIngresos(rs.getString("mes").toString(),rs.getString("monto").toString(),"");
                 lista.add(ingresos);                
             } 
             
        } catch (SQLException ex) {
            Logger.getLogger(Ingresos.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Donaciones
        sql = "SELECT MONTH(fechaDonacion) mes,SUM(monto) monto FROM donaciones WHERE fechaDonacion BETWEEN '"+fd[2]+"-"+fd[1]+"-"+fd[0]+"' AND '"+
                fh[2]+"-"+fh[1]+"-"+fh[0]+"' AND fechaDonacion IS NOT NULL GROUP BY MONTH(fechaDonacion)";
        ListaIngresos donac;
        boolean band;
        try {
            
             Statement st = cn.createStatement();
             ResultSet rs = st.executeQuery(sql);
             
             while(rs.next()){
                 totDonac=totDonac+rs.getDouble("monto");
                 band = false;
                 for(int i=0;i<lista.size();i++){
                     donac = (ListaIngresos)lista.get(i);                      
                     if(Integer.parseInt(rs.getString("mes"))==Integer.parseInt(donac.getMes())){
                         band=true;                        
                         donac.setDonacion(rs.getString("monto"));
                     }
                 }
                 if(band==false){                     
                     ListaIngresos ingresos= new ListaIngresos(rs.getString("mes").toString(),"",rs.getString("monto").toString());
                     lista.add(ingresos);
                     
                 }
                
             } 
             
        } catch (SQLException ex) {
            Logger.getLogger(Ingresos.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Ordenamos la lista
        Collections.sort(lista, new Comparator<ListaIngresos>() {
            @Override
            public int compare(ListaIngresos l1, ListaIngresos l2) {
                    return new Integer(Integer.parseInt(l1.getMes())).compareTo(new Integer(Integer.parseInt(l2.getMes())));
            }          
         });
        
        for(int i=0;i<lista.size();i++){
            donac = (ListaIngresos)lista.get(i);
            donac.setMes(retornaMes(Integer.parseInt(donac.getMes())));
                    
        }
        //Creamos el informe
        try {            
            File fichero = new File("/home/francis/NetBeansProjects/madreTeresaCRUD/src/reportes/Ingresos.jasper");            
            JasperReport reporte= (JasperReport) JRLoader.loadObject(fichero);//(JasperReport) JRLoader.loadObject("Ingresos.jasper");
           
            Map parametro = new HashMap();
            parametro.put("anio", fd[2]);   
            parametro.put("totDonac", df.format(totDonac));
            parametro.put("totCuota", df.format(totCuota));
             parametro.put("total", df.format(totCuota+totDonac));
            JasperPrint jprint= JasperFillManager.fillReport(reporte, parametro,new JRBeanCollectionDataSource(lista));
            
            JasperViewer.viewReport(jprint,false);
        } catch (JRException ex) {
            Logger.getLogger(Ingresos.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }//GEN-LAST:event_jBGenReportActionPerformed
    
    
    private String retornaMes(int mes){
        String [] meses = {"Enero","Febrero","Marzo","Abril","Mayo","Junio","Julio","Agosto","Septimbre","Octubre","Noviembre","Diciembre"};
        String retorna = meses[mes-1];
        return retorna;
    }
